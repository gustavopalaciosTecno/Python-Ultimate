import math

print(round(1.3))
print(round(1.5))
print(round(1.7))
print(abs(-77)) # abs nos entrega el valor absoluto del número

print(math.ceil(1.1)) # lleva el valor al número entero mas cercano hacia arriba
print(math.floor(1.9999999)) # lleva el valor al número entero mas cercano hacia abajo
print(math.isnan(23)) # me avisa si es un número o no lo es (solo acepta números reales)
print(math.pow(10,3)) # nos eleva un número a una potencia.
print(math.sqrt(9)) # nos saca la raiz cuadrado de un valor