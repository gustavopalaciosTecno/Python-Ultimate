def hola(nombre, apellidos="Palacios Rodriguez"): # dentro de la función se llama parámetros
    print("hola mundo")
    print(f"Bienvenido: {nombre} {apellidos}")
    
hola("Gustavo", "Palacios") # al ejecutar la función, el valor dentro de ella que le pasamos se llama argumentos  
hola("Jahziel")   







hola(apellidos="Néstor", nombre="Palacios")